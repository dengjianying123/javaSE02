public class Demo2 {

	public static void main(String[] args) {
		//人的信息
		//sysout快捷方式
		System.out.println("姚鑫");// 输出姓名:字符串
		System.out.println(18);// 年龄：int类型，整数
		System.out.println(6000.5);// 月薪:double类型,小数点
		//Shift+Ctrl+F:格式化
		System.out.println("北京西二旗");//住址：字符串 String
		//Ctrl+空格
		System.out.println("打篮球");//爱好:字符串 String
		System.out.println("男");//性别:char类型
		//......		
		
		//变量名称
	}

}
